package com.deepsoft.uisupport;

import javax.swing.JMenu;
import javax.swing.JMenuItem;
import javax.swing.JMenuItem;

public class JMenuRPH extends JMenu {
	JMenuRPH(String name) {super(name);}
	public JMenuItem getItem(String name) {
		int i;
		for (i = 0; i < getItemCount() ; i++) {
			if (name == getItem(i).getText()) return getItem(i);
		}
		return null;
	}
}



